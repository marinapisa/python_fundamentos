def soma(a:int, b:int):
    soma = a + b
    return soma

def subtracao(a:int, b:int):
    subtracao = a - b
    return subtracao

def multiplicacao(a:int, b:int):
    multiplicacao = a * b
    return multiplicacao

def divisao(a:float, b:float):
    divisao = a / b
    print(f'O resultado da divisão é: {divisao} ')
    return divisao